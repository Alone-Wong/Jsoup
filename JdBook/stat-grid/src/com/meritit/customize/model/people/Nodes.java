package com.meritit.customize.model.people;

public class Nodes {

    private String cname;
    private String code;
    private int dotcount;
    private String exp;
    private boolean ifshowcode;
    private String memo;
    private String name;
    private String nodesort;
    private int sortcode;
    private String tag;
    private String unit;
    public void setCname(String cname) {
         this.cname = cname;
     }
     public String getCname() {
         return cname;
     }

    public void setCode(String code) {
         this.code = code;
     }
     public String getCode() {
         return code;
     }

    public void setDotcount(int dotcount) {
         this.dotcount = dotcount;
     }
     public int getDotcount() {
         return dotcount;
     }

    public void setExp(String exp) {
         this.exp = exp;
     }
     public String getExp() {
         return exp;
     }

    public void setIfshowcode(boolean ifshowcode) {
         this.ifshowcode = ifshowcode;
     }
     public boolean getIfshowcode() {
         return ifshowcode;
     }

    public void setMemo(String memo) {
         this.memo = memo;
     }
     public String getMemo() {
         return memo;
     }

    public void setName(String name) {
         this.name = name;
     }
     public String getName() {
         return name;
     }

    public void setNodesort(String nodesort) {
         this.nodesort = nodesort;
     }
     public String getNodesort() {
         return nodesort;
     }

    public void setSortcode(int sortcode) {
         this.sortcode = sortcode;
     }
     public int getSortcode() {
         return sortcode;
     }

    public void setTag(String tag) {
         this.tag = tag;
     }
     public String getTag() {
         return tag;
     }

    public void setUnit(String unit) {
         this.unit = unit;
     }
     public String getUnit() {
         return unit;
     }

}